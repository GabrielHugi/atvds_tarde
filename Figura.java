public class Figura {
    private Double posicao;
    private Double altura;
    // setter
    public void setPosicao(Double posicao) {
        this.posicao = posicao;
    }
    // getter
    public Double getPosicao() {
        return this.posicao;
    }
    // setter
    public void setAltura(Double altura) {
        this.altura = altura;
    }
    // getter
    public Double getAltura() {
        return this.altura;
    }
}
